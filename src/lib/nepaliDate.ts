// Reference date: 1944-01-01 AD = 2000-09-18 BS
const refAdDate = new Date(1944, 0, 1);
const refBsYear = 2000;
const refBsMonth = 9; // Poush (0-indexed)
const refBsDay = 18;

function toNepaliDigits(num: number): string {
  const nepaliNums = ['०', '१', '२', '३', '४', '५', '६', '७', '८', '९'];
  return num.toString().split('').map(d => nepaliNums[parseInt(d)] || d).join('');
}

function getDaysInBsMonth(year: number, month: number): number {
  if (bsMonthData[year]) {
    return bsMonthData[year][month];
  }
  // Default fallback
  return 30;
}

function getTotalDaysInBsYear(year: number): number {
  if (!bsMonthData[year]) return 365;
  return bsMonthData[year].reduce((sum, days) => sum + days, 0);
}

export interface NepaliDate {
  year: number;
  month: number;
  day: number;
  dayOfWeek: number;
  monthName: { en: string; ne: string };
  dayName: { en: string; ne: string };
}

export function convertToBS(adDate: Date): NepaliDate {
  // Calculate days difference from reference date
  const diffTime = adDate.getTime() - refAdDate.getTime();
  let daysDiff = Math.floor(diffTime / (1000 * 60 * 60 * 24));
  
  let bsYear = refBsYear;
  let bsMonth = refBsMonth;
  let bsDay = refBsDay;
  
  // Add days
  while (daysDiff > 0) {
    const daysInCurrentMonth = getDaysInBsMonth(bsYear, bsMonth);
    const remainingDaysInMonth = daysInCurrentMonth - bsDay;
    
    if (daysDiff <= remainingDaysInMonth) {
      bsDay += daysDiff;
      daysDiff = 0;
    } else {
      daysDiff -= (remainingDaysInMonth + 1);
      bsMonth++;
      bsDay = 1;
      
      if (bsMonth >= 12) {
        bsMonth = 0;
        bsYear++;
      }
    }
  }
  
  // Handle negative days (dates before reference)
  while (daysDiff < 0) {
    bsDay--;
    daysDiff++;
    
    if (bsDay < 1) {
      bsMonth--;
      if (bsMonth < 0) {
        bsMonth = 11;
        bsYear--;
      }
      bsDay = getDaysInBsMonth(bsYear, bsMonth);
    }
  }
  
  const dayOfWeek = adDate.getDay();
  
  return {
    year: bsYear,
    month: bsMonth,
    day: bsDay,
    dayOfWeek,
    monthName: nepaliMonths[bsMonth],
    dayName: nepaliDays[dayOfWeek],
  };
}

export function formatNepaliDate(nepaliDate: NepaliDate, language: 'en' | 'ne'): string {
  if (language === 'ne') {
    return `${toNepaliDigits(nepaliDate.day)} ${nepaliDate.monthName.ne} ${toNepaliDigits(nepaliDate.year)}, ${nepaliDate.dayName.ne}`;
  }
  return `${nepaliDate.day} ${nepaliDate.monthName.en} ${nepaliDate.year}, ${nepaliDate.dayName.en}`;
}

export function getTodayBS(language: 'en' | 'ne' = 'en'): string {
  const today = new Date();
  const bsDate = convertToBS(today);
  return formatNepaliDate(bsDate, language);
}
